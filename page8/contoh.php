PENDING ORDER RULE
PENDING ORDER CONFIRMATION
a.	Pada Saat Market Normal
Konfirmasi Pending Order dianggap “DONE“ bila harga yang diminta/ditempatkan sesuai dengan harga yang tampil pada screen Esandar Trader 4.
b.	Pada High dan Low
High dan Low yang tampil pada sistem Esandar Trader 4 berdasarkan harga BID 
Konfirmasi Limit Order Sell berdasarkan BID
Konfirmasi Limit Order Buy berdasarkan BID+Spread
c.	Pada saat market HECTIC
Spread tidak terbatas, sehingga aturan point a dan b tidak berlaku.
LEVEL OF PENDING ORDER
Harga yang diminta/ditempatkan oleh Nasabah harus berjarak minimal 15 (lima belas) point dari harga running.

EXPIRATION OF PENDING ORDER
Pending order hanya akan berlaku sampai dengan Closing Market hari Jumat   (GTF= Good Till Friday).
Apabila kesalahan terjadi pada sistem Esandar Trader 4, maka konfirmasi pending order tergantung keputusan yang diberikan oleh Settlement pusat PT Esandar Arthamas Berjangka Jakarta.
FORCE LIQUIDATION
a.	Equity mencapai level 10%.
Apabila Equity account Nasabah mencapai level 10% atau dibawahnya dari Margin Requirement / Contract Margin maka tindakan force liquidation akan dilakukan.
b.	Short Margin untuk posisi overnight.
Semua posisi yang masih terbuka pada saat market close hari Jum’at dengan margin yang tidak cukup (short margin) akan dilikuidasi dengan harga closing yang mengacu pada harga closing Esandar Trader 4.
